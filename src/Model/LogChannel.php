namespace NgVoice\AriClient\Model;


/**
 * Details of an Asterisk log channel
 *
 * @package NgVoice\AriClient\Model
 */
class LogChannel
{
    /**
     * @var string Whether or not a log type is enabled.
     */
    private $status;

    /**
     * @var string The various log levels.
     */
    private $configuration;

    /**
     * @var string Types of logs for the log channel.
     */
    private $type;

    /**
     * @var string The log channel path.
     */
    private $channel;

    /**
     * @return string
     */
    public function getStatus(): string
    {
        return $this->status;
    }

    /**
     * @param string $status
     */
    public function setStatus(string $status): void
    {
        $this->status = $status;
    }

    /**
     * @return string
     */
    public function getConfiguration(): string
    {
        return $this->configuration;
    }

    /**
     * @param string $configuration
     */
    public function setConfiguration(string $configuration): void
    {
        $this->configuration = $configuration;
    }

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }

    /**
     * @return string
     */
    public function getChannel(): string
    {
        return $this->channel;
    }

    /**
     * @param string $channel
     */
    public function setChannel(string $channel): void
    {
        $this->channel = $channel;
    }
}